class Solution {

  public:
    int maxWater(vector<int> &arr) {
        // code here
        int res=0;
        
        for(int i=0; i<arr.size()-1; i++) {
            for(int j=i+1; j<arr.size(); j++) {
                res=max(res, (min(arr[i], arr[j])*(j-i)));
            }
        }
        
        return res;
    }
};

