class Solution {

  public:
    int maxWater(vector<int> &arr) {
        // code here
        int res=0;
        int l=0, r=arr.size()-1;
        
        while(l<r) {
            res=max(res, (min(arr[l], arr[r])*(r-l)));
            if(arr[l]<arr[r]) l++;
            else r--;
        }
        
        return res;
    }
};

