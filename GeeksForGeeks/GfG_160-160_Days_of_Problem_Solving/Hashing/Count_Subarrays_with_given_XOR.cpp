class Solution {
  public:
    long subarrayXor(vector<int> &arr, int k) {
        // code here
        int n=arr.size();
        int count=0;
        
        for(int i=0; i<n; i++) {
            for(int j=i; j<n; j++) {
                int xorr=0;
                for(int k=i; k<=j; k++) {
                    xorr=xorr^arr[k];
                }
                if(xorr==k) count++;
            }
        }
        
        return count;
    }
};

