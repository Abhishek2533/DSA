class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Code here
        int n=arr.size();
        if(n==1) return 0;
        
        int count=0;
        for(int i=0; i<n-1; i++) {
            for(int j=i+1; j<n; j++) {
                if(arr[i]+arr[j]==target) count++;
            }
        }
        
        return count;
    }
};

