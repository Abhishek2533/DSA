class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        // Code here
        unordered_map<int, int> mpp;
        int count=0;
        for(int i=0; i<arr.size(); i++) {
            int rem = target-arr[i];
            if(mpp.find(rem)!=mpp.end()) count+=mpp[rem];
            mpp[arr[i]]++;
        }
        
        return count;
    }
};

