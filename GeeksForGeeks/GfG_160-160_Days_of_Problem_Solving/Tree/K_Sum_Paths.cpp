class Solution {
  public:
    int countPath(Node* node, int k, int currSum) {
        if(node==NULL) return 0;
        int cnt=0;
        currSum+=node->data;
        
        if(currSum==k) cnt++;
        
        cnt+=countPath(node->left, k, currSum);
        cnt+=countPath(node->right, k, currSum);
        
        return cnt;
    }
    
    int sumK(Node *root, int k) {
        // code here
        if(root==NULL) return 0;
        
        int res = countPath(root, k, 0);
        
        res+=sumK(root->left, k);
        res+=sumK(root->right, k);
        
        return res;
    }
};


